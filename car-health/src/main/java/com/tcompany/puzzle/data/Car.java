package com.tcompany.puzzle.data;

public class Car {
	
	private int engineHealth;
	private int brakeHealth;
	private int transmissionHealth;
	
	
	public int getEngineHealth() {
		return engineHealth;
	}
	public void setEngineHealth(int engineHealth) {
		this.engineHealth = engineHealth;
	}
	public int getBrakeHealth() {
		return brakeHealth;
	}
	public void setBrakeHealth(int brakeHealth) {
		this.brakeHealth = brakeHealth;
	}
	public int getTransmissionHealth() {
		return transmissionHealth;
	}
	public void setTransmissionHealth(int transmissionHealth) {
		this.transmissionHealth = transmissionHealth;
	}
	
	

}
